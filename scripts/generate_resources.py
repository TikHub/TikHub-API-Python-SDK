#!/usr/bin/env python3
"""Generate resource modules from spec/openapi.json.

Reads the OpenAPI spec, groups operations by tag, and emits one Python file
per tag in src/tikhub/resources/. Each file contains a sync and an async
class with one method per operation.

Naming rules (PLAN_V3.md §4 §7):

* Resource attribute = OpenAPI tag, lowercased, dashes -> underscores, ``-API`` stripped.
  ``Douyin-Web-API`` -> ``client.douyin_web``.
* Method name = the last segment of the path, verbatim.
  ``/api/v1/douyin/web/fetch_one_video`` -> ``fetch_one_video``.
* Parameter names = the OpenAPI parameter names, verbatim.

The script also rewrites ``src/tikhub/client.py`` and
``src/tikhub/async_client.py`` so every resource is wired up as a flat
attribute on the client.

Run from the repo root::

    python scripts/generate_resources.py
"""

from __future__ import annotations

import json
from collections import defaultdict
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parent.parent
SPEC_PATH = ROOT / "spec" / "openapi.json"
RESOURCES_DIR = ROOT / "src" / "tikhub" / "resources"
CLIENT_PATH = ROOT / "src" / "tikhub" / "client.py"
ASYNC_CLIENT_PATH = ROOT / "src" / "tikhub" / "async_client.py"

# Files in resources/ that are hand-written and must never be regenerated.
PROTECTED = {"__init__.py", "_base.py"}

GENERATED_HEADER = (
    "# AUTO-GENERATED by scripts/generate_resources.py - do not edit by hand.\n"
    "# Run the script again to refresh from spec/openapi.json.\n"
)


# ---------------------------------------------------------------------------
# Spec parsing helpers
# ---------------------------------------------------------------------------


def load_spec() -> dict[str, Any]:
    with SPEC_PATH.open() as f:
        return json.load(f)


def tag_to_attr(tag: str) -> str:
    """``Douyin-Web-API`` -> ``douyin_web``; ``iOS-Shortcut`` -> ``ios_shortcut``."""
    if tag.endswith("-API"):
        tag = tag[:-4]
    return tag.replace("-", "_").lower()


def attr_to_class(attr: str) -> str:
    """``douyin_web`` -> ``DouyinWeb``."""
    return "".join(p.capitalize() for p in attr.split("_"))


def py_type(schema: dict[str, Any] | None) -> str:
    """Map an OpenAPI schema to a (loose) Python type annotation."""
    if not schema:
        return "Any"
    if "anyOf" in schema or "oneOf" in schema or "allOf" in schema:
        return "Any"
    t = schema.get("type")
    if t == "string":
        return "str"
    if t == "integer":
        return "int"
    if t == "number":
        return "float"
    if t == "boolean":
        return "bool"
    if t == "array":
        return "list[Any]"
    if t == "object":
        return "dict[str, Any]"
    return "Any"


def basename(path: str) -> str:
    return path.rstrip("/").split("/")[-1]


def collect_operations(spec: dict[str, Any]) -> dict[str, list[dict[str, Any]]]:
    """Group operations by tag.

    Returns ``{tag: [op_record, ...]}`` where each ``op_record`` is::

        {"http_method": "GET", "path": "/api/v1/...", "op": <openapi op dict>}

    Within a tag, identical ``(http_method, path)`` pairs are deduplicated
    (handles spec quirks where the same operation is listed twice).
    """
    by_tag: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for path, methods in spec["paths"].items():
        for http_method, op in methods.items():
            if http_method.lower() not in {"get", "post", "put", "delete", "patch"}:
                continue
            seen_tags: list[str] = []
            for tag in op.get("tags") or ["_untagged"]:
                if tag not in seen_tags:
                    seen_tags.append(tag)
            for tag in seen_tags:
                by_tag[tag].append(
                    {
                        "http_method": http_method.upper(),
                        "path": path,
                        "op": op,
                    }
                )

    # Dedupe identical (method, path) within a tag.
    for tag, ops in by_tag.items():
        seen: set[tuple[str, str]] = set()
        unique: list[dict[str, Any]] = []
        for entry in ops:
            key = (entry["http_method"], entry["path"])
            if key in seen:
                continue
            seen.add(key)
            unique.append(entry)
        by_tag[tag] = unique

    return dict(by_tag)


def assign_method_names(ops: list[dict[str, Any]]) -> None:
    """Set ``method_name`` on each op record, resolving collisions by adding
    parent path segments until every name is unique within the tag.
    """
    by_basename: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for entry in ops:
        by_basename[basename(entry["path"])].append(entry)

    for bn, group in by_basename.items():
        if len(group) == 1:
            group[0]["method_name"] = bn
            continue
        # Real collision: peel parent segments until each name is unique.
        for n_extra in range(1, 8):
            candidates: dict[str, list[dict[str, Any]]] = {}
            for entry in group:
                segs = entry["path"].rstrip("/").split("/")
                if len(segs) >= n_extra + 1:
                    name = "_".join(segs[-(n_extra + 1) :])
                else:
                    name = "_".join(s for s in segs if s)
                candidates.setdefault(name, []).append(entry)
            if all(len(v) == 1 for v in candidates.values()):
                for name, lst in candidates.items():
                    lst[0]["method_name"] = name
                break
        else:  # pragma: no cover
            raise RuntimeError(f"Cannot disambiguate basename {bn!r}: {group}")


def collect_parameters(
    op: dict[str, Any], spec: dict[str, Any]
) -> tuple[list[dict[str, Any]], list[dict[str, Any]], str | None]:
    """Return ``(query_params, body_params, body_kind)``.

    ``body_kind`` is one of:

    * ``None`` — no body
    * ``"props"`` — JSON object body with named properties (``body_params`` populated)
    * ``"array"`` — JSON array body (a single positional argument)
    * ``"scalar"`` — JSON body with no resolvable schema (single ``body`` arg)
    * ``"multipart"`` — multipart/form-data (``body_params`` populated)
    """
    query_params: list[dict[str, Any]] = []
    for p in op.get("parameters") or []:
        if p.get("in") != "query":
            continue
        query_params.append(
            {
                "name": p["name"],
                "schema": p.get("schema") or {},
                "required": bool(p.get("required", False)),
            }
        )

    rb = op.get("requestBody") or {}
    content = rb.get("content") or {}
    if not content:
        return query_params, [], None

    def _props_from(sch: dict[str, Any]) -> tuple[list[dict[str, Any]], dict[str, Any]]:
        if "$ref" in sch:
            ref_name = sch["$ref"].split("/")[-1]
            sch = spec["components"]["schemas"].get(ref_name, {})
        return (
            [
                {
                    "name": pname,
                    "schema": pdef,
                    "required": pname in (sch.get("required") or []),
                }
                for pname, pdef in (sch.get("properties") or {}).items()
            ],
            sch,
        )

    if "multipart/form-data" in content:
        body_params, _ = _props_from(content["multipart/form-data"]["schema"])
        return query_params, body_params, "multipart"

    if "application/json" in content:
        sch = content["application/json"]["schema"]
        if "$ref" in sch:
            ref_name = sch["$ref"].split("/")[-1]
            sch = spec["components"]["schemas"].get(ref_name, {})
        if sch.get("type") == "array":
            return query_params, [], "array"
        body_params, sch = _props_from(sch)
        if body_params:
            return query_params, body_params, "props"
        return query_params, [], "scalar"

    return query_params, [], None


# ---------------------------------------------------------------------------
# Code rendering
# ---------------------------------------------------------------------------


def _safe_param_name(name: str) -> str:
    """Avoid Python keyword / builtin shadowing.

    The TikHub spec uses Python-safe identifiers (verified by inspection),
    so this is a guard rather than a transform — we only rename true keywords.
    """
    import keyword

    if keyword.iskeyword(name) or name in {"self", "cls"}:
        return name + "_"
    return name


def render_method(entry: dict[str, Any], *, is_async: bool, spec: dict[str, Any]) -> list[str]:
    method_name = entry["method_name"]
    http_method = entry["http_method"]
    path = entry["path"]
    op = entry["op"]
    summary = (op.get("summary") or "").strip()
    deprecated = bool(op.get("deprecated", False))

    query_params, body_params, body_kind = collect_parameters(op, spec)

    # Build signature lines.
    required_lines: list[str] = []
    optional_lines: list[str] = []

    for p in query_params:
        n = _safe_param_name(p["name"])
        py_t = py_type(p["schema"])
        if p["required"]:
            required_lines.append(f"        {n}: {py_t},")
        else:
            optional_lines.append(f"        {n}: {py_t} | None = None,")

    if body_kind in ("props", "multipart"):
        for p in body_params:
            n = _safe_param_name(p["name"])
            py_t = py_type(p["schema"])
            if body_kind == "multipart" and p["schema"].get("format") == "binary":
                py_t = "Any"  # bytes / file-like / (filename, content) tuple
            if p["required"]:
                required_lines.append(f"        {n}: {py_t},")
            else:
                optional_lines.append(f"        {n}: {py_t} | None = None,")
    elif body_kind == "array":
        required_lines.append("        body: list[Any],")
    elif body_kind == "scalar":
        optional_lines.append("        body: Any = None,")

    sig_lines = required_lines + optional_lines

    # Build call body.
    body_lines: list[str] = []
    if query_params:
        body_lines.append("        params = _drop_none({")
        for p in query_params:
            n = _safe_param_name(p["name"])
            body_lines.append(f'            "{p["name"]}": {n},')
        body_lines.append("        })")

    if body_kind == "props":
        body_lines.append("        json_body = _drop_none({")
        for p in body_params:
            n = _safe_param_name(p["name"])
            body_lines.append(f'            "{p["name"]}": {n},')
        body_lines.append("        })")
    elif body_kind in ("array", "scalar"):
        body_lines.append("        json_body = body")

    if body_kind == "multipart":
        body_lines.append("        files = _drop_none({")
        for p in body_params:
            n = _safe_param_name(p["name"])
            body_lines.append(f'            "{p["name"]}": {n},')
        body_lines.append("        })")

    call_args = [f'"{http_method}"', f'"{path}"']
    if query_params:
        call_args.append("params=params")
    if body_kind in ("props", "array", "scalar"):
        call_args.append("json=json_body")
    if body_kind == "multipart":
        call_args.append("files=files")
    aw = "await " if is_async else ""
    call = f"{aw}self._client._request({', '.join(call_args)})"
    body_lines.append(f"        return {call}")

    # Docstring.
    doc_lines: list[str] = []
    title = summary or f"{http_method} {path}"
    title = title.replace("\n", " ").strip()
    doc_lines.append(f'        """{title}')
    doc_lines.append("")
    doc_lines.append(f"        ``{http_method} {path}``")
    if deprecated:
        doc_lines.append("")
        doc_lines.append("        .. deprecated:: this endpoint is marked deprecated upstream.")
    doc_lines.append('        """')

    # Assemble.
    a = "async " if is_async else ""
    out: list[str] = []
    out.append(f"    {a}def {method_name}(")
    out.append("        self,")
    if sig_lines:
        out.append("        *,")
        out.extend(sig_lines)
    out.append("    ) -> Any:")
    out.extend(doc_lines)
    out.extend(body_lines)
    return out


def render_resource_file(tag: str, attr: str, ops: list[dict[str, Any]], spec: dict[str, Any]) -> str:
    sync_cls = attr_to_class(attr)
    async_cls = "Async" + sync_cls

    lines: list[str] = []
    lines.append(GENERATED_HEADER.rstrip())
    lines.append('"""' + tag + " resource.")
    lines.append("")
    lines.append(f"OpenAPI tag: ``{tag}``")
    lines.append(f"SDK attribute: ``client.{attr}`` / ``async_client.{attr}``")
    lines.append("")
    lines.append(f"Endpoints: {len(ops)}")
    lines.append('"""')
    lines.append("")
    lines.append("from __future__ import annotations")
    lines.append("")
    lines.append("from typing import Any")
    lines.append("")
    lines.append("from tikhub.resources._base import AsyncResource, SyncResource")
    lines.append("")
    lines.append("")
    lines.append("def _drop_none(d: dict[str, Any]) -> dict[str, Any]:")
    lines.append('    """Filter out keys whose value is ``None``."""')
    lines.append("    return {k: v for k, v in d.items() if v is not None}")
    lines.append("")
    lines.append("")
    sorted_all = sorted([sync_cls, async_cls])
    lines.append(f'__all__ = ["{sorted_all[0]}", "{sorted_all[1]}"]')
    lines.append("")
    lines.append("")
    lines.append(f"class {sync_cls}(SyncResource):")
    lines.append(f'    """Sync ``{tag}`` resource ({len(ops)} endpoints)."""')
    lines.append("")

    if not ops:
        lines.append("    pass")
        lines.append("")
    else:
        for entry in ops:
            lines.extend(render_method(entry, is_async=False, spec=spec))
            lines.append("")

    lines.append("")
    lines.append(f"class {async_cls}(AsyncResource):")
    lines.append(f'    """Async ``{tag}`` resource ({len(ops)} endpoints)."""')
    lines.append("")

    if not ops:
        lines.append("    pass")
        lines.append("")
    else:
        for entry in ops:
            lines.extend(render_method(entry, is_async=True, spec=spec))
            lines.append("")

    return "\n".join(lines).rstrip() + "\n"


def render_client_module(
    *,
    is_async: bool,
    resources: list[tuple[str, str, str, str]],
) -> str:
    """Render src/tikhub/client.py or src/tikhub/async_client.py.

    ``resources`` is a list of ``(attr, sync_cls, async_cls, module)`` tuples.
    """
    cls_name = "AsyncTikHub" if is_async else "TikHub"
    base_cls = "AsyncBaseClient" if is_async else "BaseClient"
    httpx_kind = "AsyncClient" if is_async else "Client"
    transport_kind = "AsyncBaseTransport" if is_async else "BaseTransport"

    lines: list[str] = []
    lines.append(GENERATED_HEADER.rstrip())
    lines.append('"""' + ("Asynchronous" if is_async else "Synchronous") + " TikHub client.")
    lines.append("")
    lines.append("Resource attributes are wired up in ``__init__`` - one per OpenAPI tag.")
    lines.append("Method signatures match ``" + ("TikHub" if is_async else "AsyncTikHub") + "`` apart from ``async``/``await``.")
    lines.append('"""')
    lines.append("")
    lines.append("from __future__ import annotations")
    lines.append("")
    lines.append("import httpx")
    lines.append("")
    lines.append(f"from tikhub._base_client import DEFAULT_TIMEOUT, {base_cls}")

    # Imports for resources, sorted by attribute for stable diffs.
    for _attr, sync_cls, async_cls, module in sorted(resources):
        cls = async_cls if is_async else sync_cls
        lines.append(f"from tikhub.resources.{module} import {cls}")

    lines.append("")
    lines.append(f'__all__ = ["{cls_name}"]')
    lines.append("")
    lines.append("")
    lines.append(f"class {cls_name}({base_cls}):")
    if is_async:
        lines.append('    """Asynchronous TikHub API client.')
        lines.append("")
        lines.append("    Example::")
        lines.append("")
        lines.append("        import asyncio")
        lines.append("        from tikhub import AsyncTikHub")
        lines.append("")
        lines.append("        async def main():")
        lines.append('            async with AsyncTikHub(api_key="YOUR_API_KEY") as client:')
        lines.append('                video = await client.douyin_web.fetch_one_video(aweme_id="...")')
        lines.append("                print(video)")
        lines.append("")
        lines.append("        asyncio.run(main())")
        lines.append('    """')
    else:
        lines.append('    """Synchronous TikHub API client.')
        lines.append("")
        lines.append("    Example::")
        lines.append("")
        lines.append("        from tikhub import TikHub")
        lines.append("")
        lines.append('        with TikHub(api_key="YOUR_API_KEY") as client:')
        lines.append('            video = client.douyin_web.fetch_one_video(aweme_id="...")')
        lines.append("            print(video)")
        lines.append("")
        lines.append("    The constructor reads ``$TIKHUB_API_KEY`` if ``api_key`` is omitted.")
        lines.append('    """')
    lines.append("")
    lines.append("    def __init__(")
    lines.append("        self,")
    lines.append("        api_key: str | None = None,")
    lines.append("        *,")
    lines.append("        timeout: float | None = DEFAULT_TIMEOUT,")
    lines.append("        base_url: str | None = None,")
    lines.append("        max_retries: int = 3,")
    lines.append("        proxy: str | None = None,")
    lines.append("        user_agent: str | None = None,")
    lines.append("        parse_response: bool = True,")
    lines.append(f"        http_client: httpx.{httpx_kind} | None = None,")
    lines.append(f"        transport: httpx.{transport_kind} | None = None,")
    lines.append("    ) -> None:")
    lines.append("        super().__init__(")
    lines.append("            api_key,")
    lines.append("            timeout=timeout,")
    lines.append("            base_url=base_url,")
    lines.append("            max_retries=max_retries,")
    lines.append("            proxy=proxy,")
    lines.append("            user_agent=user_agent,")
    lines.append("            parse_response=parse_response,")
    lines.append("            http_client=http_client,")
    lines.append("            transport=transport,")
    lines.append("        )")
    lines.append("")
    lines.append("        # ----- Resources (one attribute per OpenAPI tag) ------------------")
    for attr, sync_cls, async_cls, _module in sorted(resources):
        cls = async_cls if is_async else sync_cls
        lines.append(f"        self.{attr}: {cls} = {cls}(self)")
    lines.append("")
    lines.append("    def __repr__(self) -> str:")
    lines.append(f'        return f"{cls_name}(base_url={{self._base_url!r}})"')
    lines.append("")

    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------


def main() -> None:
    spec = load_spec()
    by_tag = collect_operations(spec)

    print(f"Loaded spec V{spec['info'].get('version')} with {sum(len(v) for v in by_tag.values())} operations across {len(by_tag)} tags.")

    # Pre-compute method names per tag.
    for ops in by_tag.values():
        assign_method_names(ops)

    # Drop any existing generated files (preserve PROTECTED + the per-resource
    # files we are about to overwrite).
    for f in RESOURCES_DIR.iterdir():
        if f.is_file() and f.name not in PROTECTED and f.suffix == ".py":
            f.unlink()

    # Render resource files.
    resources_meta: list[tuple[str, str, str, str]] = []
    for tag in sorted(by_tag):
        ops = by_tag[tag]
        attr = tag_to_attr(tag)
        sync_cls = attr_to_class(attr)
        async_cls = "Async" + sync_cls
        module = attr  # filename = attr name
        path = RESOURCES_DIR / f"{module}.py"
        path.write_text(render_resource_file(tag, attr, ops, spec))
        resources_meta.append((attr, sync_cls, async_cls, module))
        print(f"  wrote {path.relative_to(ROOT)}  ({len(ops)} endpoints)")

    # Render client.py and async_client.py.
    CLIENT_PATH.write_text(render_client_module(is_async=False, resources=resources_meta))
    ASYNC_CLIENT_PATH.write_text(render_client_module(is_async=True, resources=resources_meta))
    print(f"  wrote {CLIENT_PATH.relative_to(ROOT)}")
    print(f"  wrote {ASYNC_CLIENT_PATH.relative_to(ROOT)}")

    print(f"\nDone. {len(resources_meta)} resources, {sum(len(v) for v in by_tag.values())} endpoints.")


if __name__ == "__main__":
    main()
